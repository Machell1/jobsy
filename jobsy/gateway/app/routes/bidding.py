async def _get_profile_name(db: AsyncSession, user_id: str) -> str:
    """Look up display_name from profiles table, fall back to user_id."""
    result = await db.execute(
        text("SELECT display_name FROM profiles WHERE user_id = :uid"),
        {"uid": user_id},
    )
    row = result.first()
    return row[0] if row and row[0] else user_id


async def _notify_providers_for_category(
    db: AsyncSession,
    job_post: JobPost,
    hirer_name: str,
) -> None:
    """Insert notification_logs for providers matching the job's category."""
    result = await db.execute(
        text(
            "SELECT user_id FROM profiles "
            "WHERE service_category = :cat AND user_id != :hirer"
        ),
        {"cat": job_post.category, "hirer": job_post.hirer_id},
    )
    provider_rows = result.all()

    budget_str = ""
    if job_post.budget_min and job_post.budget_max:
        budget_str = f" — Budget: J${job_post.budget_min}-${job_post.budget_max}"
    elif job_post.budget_max:
        budget_str = f" — Budget: up to J${job_post.budget_max}"

    now = datetime.now(UTC)
    for row in provider_rows:
        provider_user_id = row[0]
        await db.execute(
            text(
                "INSERT INTO notification_logs "
                "(id, user_id, title, body, data, notification_type, "
                "delivered, read, sent_at) "
                "VALUES (:id, :uid, :title, :body, :data::jsonb, :ntype, "
                "false, false, :sent_at)"
            ),
            {
                "id": str(uuid.uuid4()),
                "uid": provider_user_id,
                "title": f"New Job in {job_post.category}!",
                "body": (
                    f"{hirer_name} posted: {job_post.title}{budget_str}"
                ),
                "data": f'{{"type":"job_post","job_post_id":"{job_post.id}"}}',
                "ntype": "job_post",
                "sent_at": now,
            },
        )


