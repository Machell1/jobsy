@router.post("/contracts/{contract_id}/sign")
async def sign_contract(
    contract_id: str,
    data: ContractSign,
    request: Request,
    user: dict = Depends(get_current_user),
    db: AsyncSession = Depends(get_db),
):
    """Digitally sign a contract (ETA 2006 compliant)."""
    result = await db.execute(
        select(Contract).where(Contract.id == contract_id),
    )
    contract = result.scalar_one_or_none()
    if not contract:
        raise HTTPException(status_code=404, detail="Contract not found")

    uid = user["user_id"]
    if contract.hirer_id != uid and contract.provider_id != uid:
        raise HTTPException(status_code=403, detail="Not authorized")

    # Determine signer role
    signer_role = "hirer" if uid == contract.hirer_id else "provider"

    # Check not already signed by this user
    existing = await db.execute(
        select(ContractSignature).where(
            ContractSignature.contract_id == contract_id,
            ContractSignature.signer_id == uid,
        ),
    )
    if existing.scalar_one_or_none():
        raise HTTPException(status_code=400, detail="Already signed")

    now = datetime.now(UTC)
    sig = ContractSignature(
        id=str(uuid.uuid4()),
        contract_id=contract_id,
        signer_id=uid,
        signer_role=signer_role,
        signature_data=data.signature_data,
        signature_method=data.signature_method,
        ip_address=request.client.host if request.client else None,
        user_agent=request.headers.get("user-agent", "")[:500],
        signed_at=now,
    )
    db.add(sig)

    # Update contract status
    if signer_role == "hirer":
        new_status = (
            "fully_signed" if contract.status == "provider_signed"
            else "hirer_signed"
        )
    else:
        new_status = (
            "fully_signed" if contract.status == "hirer_signed"
            else "provider_signed"
        )

    contract.status = new_status
    contract.updated_at = now
    await db.flush()

    if new_status == "fully_signed":
        # Notify both parties
        try:
            await _insert_notification(
                db, contract.hirer_id,
                "Contract Fully Signed",
                f"Contract for '{contract.title}' is fully signed — work can begin!",
                {"type": "contract", "contract_id": contract.id},
                "contract",
            )
            await _insert_notification(
                db, contract.provider_id,
                "Contract Fully Signed",
                f"Contract for '{contract.title}' is fully signed — work can begin!",
                {"type": "contract", "contract_id": contract.id},
                "contract",
            )
        except Exception:
            logger.exception("Failed to notify parties for contract %s", contract.id)

        await publish_event("contract.fully_signed", {
            "contract_id": contract.id,
            "job_post_id": contract.job_post_id,
            "hirer_id": contract.hirer_id,
            "provider_id": contract.provider_id,
        })

    resp = _contract_response(contract)
    sig_result = await db.execute(
        select(ContractSignature)
        .where(ContractSignature.contract_id == contract_id)
        .order_by(ContractSignature.signed_at),
    )
    resp["signatures"] = [_signature_response(s) for s in sig_result.scalars().all()]
    return resp


