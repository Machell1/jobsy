@router.post("/{job_post_id}/bids", status_code=status.HTTP_201_CREATED)
async def submit_bid(
    job_post_id: str,
    data: BidCreate,
    user: dict = Depends(get_current_user),
    db: AsyncSession = Depends(get_db),
):
    """Submit a bid on a job post (providers only)."""
    if user.get("active_role") != "provider":
        raise HTTPException(status_code=403, detail="Only providers can submit bids")

    jp_result = await db.execute(
        select(JobPost).where(JobPost.id == job_post_id),
    )
    jp = jp_result.scalar_one_or_none()
    if not jp:
        raise HTTPException(status_code=404, detail="Job post not found")
    if jp.status != "open":
        raise HTTPException(status_code=400, detail="Job post is not open for bidding")

    # Check bid deadline
    if jp.bid_deadline and datetime.now(UTC) > jp.bid_deadline:
        raise HTTPException(status_code=400, detail="Bid deadline has passed")

    # Check max bids
    if jp.max_bids:
        bc = await db.execute(
            select(func.count(Bid.id)).where(Bid.job_post_id == job_post_id),
        )
        if (bc.scalar() or 0) >= jp.max_bids:
            raise HTTPException(status_code=400, detail="Maximum number of bids reached")

    # Check duplicate
    existing = await db.execute(
        select(Bid).where(
            Bid.job_post_id == job_post_id,
            Bid.provider_id == user["user_id"],
        ),
    )
    if existing.scalar_one_or_none():
        raise HTTPException(status_code=400, detail="You have already bid on this job")

    now = datetime.now(UTC)
    bid = Bid(
        id=str(uuid.uuid4()),
        job_post_id=job_post_id,
        provider_id=user["user_id"],
        amount=data.amount,
        currency=data.currency,
        proposal=data.proposal,
        estimated_duration_days=data.estimated_duration_days,
        available_start_date=(
            date_type.fromisoformat(data.available_start_date)
            if data.available_start_date else None
        ),
        attachments=data.attachments,
        status="submitted",
        created_at=now,
        updated_at=now,
    )
    db.add(bid)
    await db.flush()

    # Notify hirer
    try:
        await _insert_notification(
            db, jp.hirer_id,
            f"New Bid on {jp.title}",
            f"A provider submitted a bid of {data.currency} {data.amount:,.2f}",
            {"type": "bid", "job_post_id": job_post_id, "bid_id": bid.id},
            "bid",
        )
    except Exception:
        logger.exception("Failed to notify hirer for bid %s", bid.id)

    await publish_event("bid.submitted", {
        "bid_id": bid.id,
        "job_post_id": job_post_id,
        "provider_id": user["user_id"],
        "hirer_id": jp.hirer_id,
        "amount": str(data.amount),
    })

    return _bid_response(bid)


